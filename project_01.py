# Celsisus to Fahrenheit Convertor

temp_in_celsius = int(input("Enter the temperature in celsisus: "))

# # F = 1.8 C + 32

temp_in_fahrenheit = round(1.8*temp_in_celsius+32,2)

print(f"Temperature in fahrenheit is {temp_in_fahrenheit}")


# This program take celsius as input and convert in into fahrenheit

